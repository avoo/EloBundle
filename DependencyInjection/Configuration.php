namespace Avoo\EloBundle\DependencyInjection;

use Symfony\Component\Config\Definition\Builder\ArrayNodeDefinition;
use Symfony\Component\Config\Definition\Builder\TreeBuilder;
use Symfony\Component\Config\Definition\ConfigurationInterface;

class Configuration implements ConfigurationInterface
{
    public function getConfigTreeBuilder()
    {
        $treeBuilder = new TreeBuilder();
        $rootNode = $treeBuilder->root('avoo_elo');

        $this->addServicesSection($rootNode);

        return $treeBuilder;
    }

    /**
     * Adds 'services' section.
     *
     * @param ArrayNodeDefinition $node
     */
    public function addServicesSection(ArrayNodeDefinition $node)
    {
        $node
            ->addDefaultsIfNotSet()
            ->children()
                ->arrayNode('service')
                    ->addDefaultsIfNotSet()
                    ->children()
                        ->scalarNode('configuration')->defaultValue('avoo_elo.configuration.default')->end()
                        ->scalarNode('aggregation')->defaultValue('avoo_elo.aggregation.default')->end()
                        ->scalarNode('calculator')->defaultValue('avoo_elo.calculator.default')->end()
                    ->end()
                ->end()
            ->end();
    }
}
